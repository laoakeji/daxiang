    <h4 style="text-align:center;font-size: 33px">东莞市环保局：东莞市出台家具制造及制鞋行业VOCs污染整治工作实施方案</h4>
    <div class="clear" style="height:30px;"></div>
    <div style="margin-top:5px;margin-bottom:20px;"><div><b>日期: </b>2016-08-31 00:00:00</div></div>
	  <div class="content">
<p>东莞市环保局：东莞市出台家具制造及制鞋行业VOCs污染整治工作实施方案</p><p>为进一步改善大气环境质量，有效削减VOCs排放总量，精准防控臭氧污染，东莞市出台家具制造及制鞋行业VOCs污染整治工作实施方案，以家具制造及制鞋行业企业为重点，大力开展VOCs污染整治。</p><p>一是从严管控。在自然保护区、水源保护区、风景名胜区、森林公园、重要湿地、生态敏感区和其他重要生态功能区实行强制性保护禁止新建家具制造及制鞋行业VOCs排放企业，并逐步清理现有污染源。将全市细分为禁止准入区域、严格控制区域和一般控制区域，实施VOCs总量控制和区域限批政策，其中严格控制区VOCs排放项目须实施“2倍总量替代”、一般控制区域VOCs排放项目实施“减量替代”。新、改、扩建家具制造项目使用低VOCs含量涂料的比例不得低于90%；所有排放VOCs的车间或工段应采取密闭等措施，净化效率不得低于80%。</p><p>二是全面治理。按照“提升一批、整治一批、淘汰一批”的原则，对全市梳理出来的家具制造及制鞋行业VOCs排放企业进行分类处理，提升企业治污水平。对提升类企业，采取鼓励与强制相结合原则，鼓励为主，推动企业进行治污改造，提升生产水平；对整治类企业，采取强制与鼓励相结合原则，强制为主，推动企业完善环保手续，整改存在问题，确保符合环保要求，有效削减VOCs排放量；对淘汰类企业，依法予以淘汰关闭。</p><p>三是强化监管。每年组织购买社会检测服务，对VOCs重点监管企业每年进行一次抽样检测，评估其净化效率。对虽建有VOCs治理设施，但运行维护存在重大缺陷、净化效率达不到要求的探索实施第三方运营维护管理政策。完善信息公开制度，向社会公开整治企业名单、VOCs污染源排查、整治结果等，引导和鼓励公众积极参与企业环境监督。建立企业环境信息强制公开制度，企业应主动公开涉VOCs原辅料种类、污染物排放、治理设施运行、VOCs申报等相关环境信息，接受社会监督。</p><p>四是加强引导。编制《家具制造及制鞋行业VOCs治理技术指南》，作为相关企业开展整治工作的参考依据。研究出台VOCs治理财政补助政策，以及积极利用国家及省市有关财政资金，通过采取“以奖代补”、“以奖促防”、“以奖促治”等方式，加快VOCs整治工作。</p><p><br></p>        </div>
